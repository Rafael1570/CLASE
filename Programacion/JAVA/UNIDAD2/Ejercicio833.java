/*Ejercicio propuesto 8.3.3: Crea una función que devuelva la primera letra de una cadena de texto.
Probar esta función para calcular la primera letra de la frase "Hola". */


import java.util.Scanner;
public class Ejercicio833 {
    public static void main(String[] args) {
        String hola = "Hola";
        System.out.println(hola.charAt(0));
        System.out.println(hola.charAt(1));
        System.out.println(hola.charAt(2));
        System.out.println(hola.charAt(3));
    }
}
